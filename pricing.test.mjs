/* Крихітний тест геометрії/ціни — страховка від регресій формули площі.
   Запуск: node pricing.test.mjs  (вихід 1 при будь-якому розходженні). */
import BZ from './pricing.js';

let fails = 0;
function eq(name, got, exp, tol){
  tol = tol || 1e-9;
  const ok = Math.abs(got - exp) <= tol;
  console.log((ok ? '✓' : '✗') + ' ' + name + '  → ' + got + (ok ? '' : '   ОЧІКУВАВ ' + exp));
  if (!ok) fails++;
}

// 1) між, 3×650 двері, фасад 4000, глибина 1200, H2100 → 2 перегородки → 13.44 м²
eq('між · 3 двері · площа', BZ.cabinArea(4000, 1200, 3, 'між', 2100), 13.44);

// 2) кут, 3×650, 4000×1200, H2100 → 3 перегородки → 15.96 м² (історичний приклад AppSheet) → 2713.20 €
eq('кут · 3 двері · площа', BZ.cabinArea(4000, 1200, 3, 'кут', 2100), 15.96);
eq('кут · 3 двері · ціна', BZ.cost(BZ.cabinArea(4000, 1200, 3, 'кут', 2100), 170), 2713.20, 1e-6);

// 3) окрема, 2×700, 3000×1500, H2100 → 3 перегородки → 15.75 м²
eq('окр · 2 двері · площа', BZ.cabinArea(3000, 1500, 2, 'окр', 2100), 15.75);

// 4) мін. ширина кабінки + к-сть перегородок
eq('cabinMinW(600)', BZ.cabinMinW(600), 1100);
eq('cabinMinW(900)', BZ.cabinMinW(900), 1300);
eq('parts(3, між)', BZ.parts(3, 'між'), 2);
eq('parts(3, кут)', BZ.parts(3, 'кут'), 3);
eq('parts(3, окр)', BZ.parts(3, 'окр'), 4);
eq('parts(0, кут)', BZ.parts(0, 'кут'), 0);

// 5) пісуар: 2 панелі × 550 × 1545 → 1.6995 м² (НЕ по висоті кабіни 2100)
eq('пісуар · 2 панелі · площа', BZ.panelArea(2, 550, 1545), 1.6995);

// 6) Tube (H2000), кут, 4×600, 4000×1200 → 4 перегородки → 17.6 м²
eq('tube кут · 4 двері · площа', BZ.cabinArea(4000, 1200, 4, 'кут', 2000), 17.6);

console.log(fails ? ('\n❌ ' + fails + ' ТЕСТ(И) ВПАЛИ') : '\n✅ УСІ ТЕСТИ ЦІНИ ПРОЙДЕНІ');
process.exit(fails ? 1 : 0);
